package com.example.recwithfrag;

class MyListData {
    private String info;
    private int sdf;

    public MyListData(String email, int ic_launcher_background) {
        this.info=email;
        this.sdf=ic_launcher_background;

    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public int getSdf() {
        return sdf;
    }

    public void setSdf(int sdf) {
        this.sdf = sdf;
    }

}
